/**
 * タイポグラフィ定義。
 *
 * Material Design 3 に準拠したタイポグラフィスケールを提供します。
 */
@Immutable
data class SortTypography(
    val displayLarge: TextStyle,
    val displayMedium: TextStyle,
    val displaySmall: TextStyle,
    val headlineLarge: TextStyle,
    val headlineMedium: TextStyle,
    val headlineSmall: TextStyle,
    val titleLarge: TextStyle,
    val titleMedium: TextStyle,
    val titleSmall: TextStyle,
    val bodyLarge: TextStyle,
    val bodyMedium: TextStyle,
    val bodySmall: TextStyle,
    val labelLarge: TextStyle,
    val labelMedium: TextStyle,
    val labelSmall: TextStyle,
)

/**
 * アプリケーションのフォントファミリー。
 */
@Composable
fun getSortFontFamily(): FontFamily {
    return FontFamily(
        org.jetbrains.compose.resources.Font(dotnet.sort.designsystem.generated.resources.Res.font.notosans_jp_regular, FontWeight.Normal),
        org.jetbrains.compose.resources.Font(dotnet.sort.designsystem.generated.resources.Res.font.notosans_jp_regular, FontWeight.Medium),
        // Add more weights if downloaded, otherwise map to Regular
    )
}

